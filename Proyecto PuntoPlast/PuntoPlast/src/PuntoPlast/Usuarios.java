    private void mneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mneliminarActionPerformed

        //linea para eliminar cuentas de la base de datos
        int fila= tbusuario.getSelectedRow();
        String usuario= tbusuario.getValueAt(fila, 0).toString();
        
        if(fila<0){
            JOptionPane.showMessageDialog(this, "Seleccione alguna fila");
        }else{                       
            if(!usuario.equals(log.nombre)){//Si el usuario seleccionado no es el actual
                Confirmar c=new Confirmar(usuario);
                c.toFront();
            } else {
                JOptionPane.showMessageDialog(this, "Error: No se puede borrar el usuario actual.");
            }
        }
    }//GEN-LAST:event_mneliminarActionPerformed

