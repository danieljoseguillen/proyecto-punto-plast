    public static void mostrarusuarios(){
        // linea de sql en la que se muestran las cuentas en pantalla
        String [] titulos= {"Usuario", "Privilegio"};
        modelo=new  DefaultTableModel(null,titulos);   
        
        String datos []=new String[2];
        String sql="SELECT * FROM  usuario WHERE Tipo_usu='Administrador' OR Tipo_usu='Invitado'"; 
        
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                datos[0]=rs.getString("Id_usu");
                datos[1]=rs.getString("Tipo_usu");
                
                modelo.addRow(datos);
            }
            tbusuario.setModel(modelo);
        } catch (SQLException ex) {
            System.out.println(ex);
            Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
