private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
    //linea de registro de cuentas en la base de datos
    char[] password = txtpass.getPassword();
    String clave = new String(password);
    char[] repetir = txtrepetir.getPassword();
    String claverepetir = new String(repetir);
    
    if((txtnick.getText().trim().length()==0)||(clave.trim().length()==0)||(claverepetir.trim().length()==0)){//Si los campos estan vacios
        JOptionPane.showMessageDialog(this,"Error: Por favor llene todos los campos, Operacion cancelada.");
    }else{
        if((clave.trim().length()<4)){//Si la contraseña tiene menos de 4 caracteres
            JOptionPane.showMessageDialog(this,"Error: La contraseña debe ser minimo de 4 caracteres.");
            limpiarPass();
        }else{
            if(claverepetir.equals(clave)){//Si la contraseña es igual a repetir contraseña
                String ins="INSERT INTO usuario (Id_usu, Password_usu, Email_usu, Tipo_usu) VALUES(?,MD5(?),?,?)";
                
                try {
                    PreparedStatement pst = cn.prepareStatement(ins);
                    pst.setString(1, txtnick.getText());                    
                    pst.setString(2, clave);
                    pst.setString(3, "(Vacio)");
                    pst.setString(4, cbotipo.getSelectedItem().toString());
                    int n= pst.executeUpdate();
                    
                    if(n>0){
                        JOptionPane.showMessageDialog(this, "Se guardaron los datos exitosamente.");
                        mostrarusuarios();
                        limpiar();
                    }else{
                        JOptionPane.showMessageDialog(this, "Error, no se puedo guardar los datos.");
                    }           
                } catch (SQLException ex) {
                    Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this,"Error: La contraseña no coincide.");
                limpiarPass();
            }
        }
    }
}//GEN-LAST:event_btnregistrarActionPerformed

