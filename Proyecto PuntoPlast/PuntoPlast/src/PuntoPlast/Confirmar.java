    void eliminar(String usu){
        String eliminar="DELETE FROM usuario WHERE Id_usu='"+usu+"'";
                
            try {
                PreparedStatement pst;
                pst = cn.prepareStatement(eliminar);
                int n=pst.executeUpdate();
                
                if(n>0){
                    JOptionPane.showMessageDialog(this, "Eliminacion de registro exitosa");
                    Usuarios.mostrarusuarios();
                    Usuarios.limpiar();                    
                } else {
                    JOptionPane.showMessageDialog(this, "Operacion cancelada, no se pudo borrar el usuario.");
                    Usuarios.mostrarusuarios();
                }                    
            } catch (SQLException ex) {
                System.out.println(ex);
                Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
