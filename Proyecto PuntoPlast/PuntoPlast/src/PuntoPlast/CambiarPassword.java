    void Modificar(String usuario, String nuevaPass){
        String update="UPDATE usuario SET Password_usu=MD5('"+nuevaPass+"') WHERE Id_usu='"+usuario+"'";
        
        try{
            PreparedStatement ps=cn.prepareStatement(update);
            int n=ps.executeUpdate();
            
            if(n>0){
               JOptionPane.showMessageDialog(null, "Se ha cambiado la contraseña exitosamente.");
               
               txtactual.setText("");
               txtnuevo.setText("");
               txtrepetir.setText("");
               txtactual.requestFocus();
            }
            
        }catch(SQLException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "Operacion cancelada: No se pudo cambiar la contraseña.", "Error", JOptionPane.ERROR_MESSAGE); 
        }
        
    }
   
