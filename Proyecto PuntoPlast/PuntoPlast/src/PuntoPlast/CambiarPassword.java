    private void bnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnaceptarActionPerformed
        // TODO add your handling code here:
        char actual[]=txtactual.getPassword();
        char nueva[]=txtnuevo.getPassword();
        char repetir[]=txtrepetir.getPassword();
        
        String act=new String(actual);
        String nuevo=new String(nueva);
        String repet=new String(repetir);
        String usuario=Login.nombre;
                
        if((act.length()==0)||(nuevo.length()==0)||(repet.length()==0)){
            JOptionPane.showMessageDialog(null, "Por favor, llene todos los campos.");
        }else{
            try{
                Statement st=cn.createStatement();
                ResultSet rs=st.executeQuery("SELECT * FROM usuario WHERE Id_usu='"+Login.nombre+"' AND Password_usu=MD5('"+act+"')");
                       
                if(rs.next()){
                    if(nuevo.equals(repet)){
                        Modificar(usuario, nuevo);
                    }else{
                        JOptionPane.showMessageDialog(null, "La contraseña nueva no coincide con la confirmacion. Intente de nuevo.", "Error", JOptionPane.ERROR_MESSAGE);                    
                        txtnuevo.setText("");
                        txtrepetir.setText("");
                        txtnuevo.requestFocus();
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "La contraseña actual que ingreso es incorrecta. Intente de nuevo.", "Error", JOptionPane.ERROR_MESSAGE);
                    txtnuevo.requestFocus();
                }
            }catch(SQLException ex){
                System.out.println("Error: "+ex);
                JOptionPane.showMessageDialog(null, "Error al consultar la contraseña");
            }
        }
        
    }//GEN-LAST:event_bnaceptarActionPerformed

