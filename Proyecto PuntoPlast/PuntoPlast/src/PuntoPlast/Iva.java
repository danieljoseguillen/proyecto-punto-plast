    void cargar() {
        //carga todos los productos o los selecionados
        try{
            String cons="SELECT Porc_iva, Fecha_iva FROM IVA WHERE Fecha_iva=(SELECT MAX(Fecha_iva) FROM IVA)";//Selecciona el ultimo IVA seleccionado
            
            Statement st= cn.createStatement();
            ResultSet rs = st.executeQuery(cons);
            
            while(rs.next()){ 
                int iva= Integer.parseInt(rs.getString("Porc_iva"));
                actual.setText("Actual: "+iva+"%");
                txtiva.setText(Integer.toString(iva));
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error: No se pudo encontrar iva.");
                System.out.println(e.getMessage());
            }
    }
    
    
    
    
