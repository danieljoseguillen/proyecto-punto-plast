    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        if(txtiva.getText().trim().length()>0){//Si tiene numeros
            int value = Integer.parseInt(txtiva.getText());            
            
            try {//Busca si existe el iva tipeado
                String Porc_iva="";
                String select="SELECT Porc_iva FROM iva WHERE Porc_iva="+value+"";
                
                Statement st1= cn.createStatement();
                ResultSet rs1=st1.executeQuery(select);
                
                while(rs1.next()){
                    Porc_iva=rs1.getString("Porc_iva");                
                }
                
                if(Porc_iva.equals("")){//Si no existe el iva tipeado, lo aÃ±ade a la BD
                    try {
                        String insert="INSERT INTO iva(Porc_iva, Fecha_iva) VALUES(?, CURRENT_TIMESTAMP)";
                        
                        PreparedStatement pst= cn.prepareStatement(insert);
                        pst.setInt(1, value);
                        int n=pst.executeUpdate();
                        
                        if(n>0){
                            JOptionPane.showMessageDialog(this, "Registro Guardado con Exito");
                            cargar();
                        }                        
                        
                    }catch(Exception e){                    
                        JOptionPane.showMessageDialog(this, "Error al guardar, operacion cancelada");
                        System.out.print(e.getMessage());                    
                    }
                }else{//Si existe, actualiza fecha
                    try {
                        String update="UPDATE iva SET Fecha_iva=CURRENT_TIMESTAMP WHERE Porc_iva="+value+"";
                        
                        PreparedStatement pst= cn.prepareStatement(update);
                        int n=pst.executeUpdate();
                        
                        if(n>0){
                            JOptionPane.showMessageDialog(this, "Registro Guardado con Exito");
                            cargar();
                        }                        
                    }catch(Exception e){                    
                        JOptionPane.showMessageDialog(this, "Error al guardar, operacion cancelada");
                        System.out.print(e.getMessage());
                    }     
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al guardar, operacion cancelada");
                System.out.print(e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese un valor para el iva");
        }
    }//GEN-LAST:event_btnaceptarActionPerformed

